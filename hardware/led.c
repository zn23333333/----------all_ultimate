/*************************************************************************************************
*	๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ:	LED_Init
*
*	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	IO๏ฟฝฺณ๏ฟฝสผ๏ฟฝ๏ฟฝ
*	 
*************************************************************************************************/

void LED_Init(void)
{		
	GPIO_InitTypeDef GPIO_InitStructure; //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝแนน๏ฟฝ๏ฟฝ
	RCC_AHB1PeriphClockCmd ( LED1_CLK, ENABLE); 	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝGPIOGสฑ๏ฟฝ๏ฟฝ	
	
	GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_OUT;   //๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝ
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;  //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_UP;	//๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_2MHz; //๏ฟฝูถ๏ฟฝัก๏ฟฝ๏ฟฝ
	
	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ LED1 ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	GPIO_InitStructure.GPIO_Pin = LED1_PIN;	 
	GPIO_Init(LED1_PORT, &GPIO_InitStructure);	
	
	GPIO_ResetBits(LED1_PORT,LED1_PIN);  //PG7๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝฦฝ
}




