/*************************************************************************************************
*	๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ:	KEY_Init
*
*	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝIO๏ฟฝฺณ๏ฟฝสผ๏ฟฝ๏ฟฝ
*	 
*************************************************************************************************/

void KEY_Init(void)
{		
	GPIO_InitTypeDef GPIO_InitStructure; //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝแนน๏ฟฝ๏ฟฝ
	RCC_AHB1PeriphClockCmd ( KEY_CLK, ENABLE); 	//๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝKEYสฑ๏ฟฝ๏ฟฝ	
	
	GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_IN;   //๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝ
	GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_UP;	//๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_2MHz; //๏ฟฝูถ๏ฟฝัก๏ฟฝ๏ฟฝ
	GPIO_InitStructure.GPIO_Pin   = KEY_PIN;	 
	
	GPIO_Init(KEY_PORT, &GPIO_InitStructure);	

}

/*************************************************************************************************
*	๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ:	KEY_Scan
*
*	๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ึต:	KEY_ON - ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยฃ๏ฟฝKEY_OFF - ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลฟ๏ฟฝ
*
*	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ:	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝษจ๏ฟฝ๏ฟฝ
*	
*************************************************************************************************/
		
uint8_t	KEY_Scan(void)
{
	if( GPIO_ReadInputDataBit ( KEY_PORT,KEY_PIN) == 0 )	//๏ฟฝ๏ฟฝโฐด๏ฟฝ๏ฟฝ๏ฟฝวท๑ฑปฐ๏ฟฝ๏ฟฝ๏ฟฝ
	{	
		Delay_ms(10);	//๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
		if(GPIO_ReadInputDataBit ( KEY_PORT,KEY_PIN) == 0)	//๏ฟฝูดฮผ๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝฮช๏ฟฝอต๏ฟฝฦฝ
		{
			while(GPIO_ReadInputDataBit ( KEY_PORT,KEY_PIN) == 0);	//๏ฟฝศด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลฟ๏ฟฝ
			return KEY_ON;	//๏ฟฝ๏ฟฝ๏ฟฝุฐ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยฑ๏ฟฝึพ
		}
	}
	return KEY_OFF;	
}


