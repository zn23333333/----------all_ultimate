    GPIO_InitTypeDef GPIO_InitStructure;
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8 | GPIO_Pin_9;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;  // 魹ｽ魹ｽ魹ｽﾄ｣ﾊｽ
    GPIO_InitStructure.GPIO_OType = GPIO_OType_OD; // 魹ｽ魹ｽﾂｩ魹ｽ魹ｽ魹ｽ
